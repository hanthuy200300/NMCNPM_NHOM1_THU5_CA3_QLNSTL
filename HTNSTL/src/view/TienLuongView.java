    private void tongLuongBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tongLuongBtnActionPerformed
    	int index= tableLuongNV.getSelectedRow();
    	if (index!=-1) {
    		String maNhanVien = (String) defaultTableModel.getValueAt(index, 0);
        	Luong luong = luongService.findByMaNhanVien(maNhanVien);
        	int luongUng = Integer.parseInt(fieldLuongUng.getText());
        	int tienPhat = Integer.parseInt(fieldTienPhat.getText());
        	int tienThuong = Integer.parseInt(fieldTienThuong.getText());
        	long tongLuong = (long) (luong.getHsLuong()*luong.getLuongCb()-luongUng-tienPhat+tienThuong);
        	luong.setLuongUng(luongUng);
        	luong.setTienPhat(tienPhat);
        	luong.setTienThuong(tienThuong);
        	luong.setTongLuong(tongLuong);
        	if (luongService.update(luong)) {
				JOptionPane.showMessageDialog(this, "chốt lương thành công");
				loadData();
			}else {
				System.out.println(luong);
				JOptionPane.showMessageDialog(this, "chốt lương không thành công");

			}
        	
		}
    	
    	
    }//GEN-LAST:event_tongLuongBtnActionPerformed

