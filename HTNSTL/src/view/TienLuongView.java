    public void loadData() {
    	defaultTableModel = new DefaultTableModel();
        defaultTableModel.addColumn("Id Nhân viên");
        defaultTableModel.addColumn("Tên Nhân Viên");
        defaultTableModel.addColumn("Lương CB");
        defaultTableModel.addColumn("Hệ Số Lương");
        defaultTableModel.addColumn("Tiền Thưởng");
        defaultTableModel.addColumn("Lương Ứng");
        defaultTableModel.addColumn("Tiền Phạt");
        defaultTableModel.addColumn("Lương Tổng");
        List<Luong> data =luongService.findAll();
        for (Luong luong : data) {
			String maNhanVien = luong.getIdNhanVien();
			NhanVien nv  = nhanVienService.findById(maNhanVien);
			Vector<Object> v = new Vector<Object>();
			v.add(maNhanVien);
			v.add(nv.getTenNhanVien());
			v.add(luong.getLuongCb());
			v.add(luong.getHsLuong());
			v.add(luong.getTienThuong());
			v.add(luong.getLuongUng());
			v.add(luong.getTienPhat());
			v.add(luong.getTongLuong());
			defaultTableModel.addRow(v);
		}
        tableLuongNV.setModel(defaultTableModel);
        fieldIDNV.setEnabled(false);
        tableLuongNV.addKeyListener(new KeyAdapter() {
        	@Override
        	public void keyReleased(KeyEvent e) {
        		if(e.getKeyCode()==KeyEvent.VK_UP||e.getKeyCode()==KeyEvent.VK_DOWN) {
        			int index = tableLuongNV.getSelectedRow();
        			displayDetails(index);
        		}
        	}
		});
        tableLuongNV.addMouseListener(new MouseAdapter() {
        	@Override
        	public void mouseClicked(MouseEvent e) {
        		int index = tableLuongNV.getSelectedRow();
        		displayDetails(index);
        	}
		});
        
    }
    private void displayDetails(int index) {
    	String maNhanVien = (String) defaultTableModel.getValueAt(index, 0);
    	Luong luong=luongService.findByMaNhanVien(maNhanVien);
    	fieldIDNV.setText(luong.getIdNhanVien());
    	fieldLuongUng.setText(""+luong.getLuongUng());
    	fieldTienPhat.setText(""+luong.getTienPhat());
    	fieldTienThuong.setText(""+luong.getTienThuong());
	}
