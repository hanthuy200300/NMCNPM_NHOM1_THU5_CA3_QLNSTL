    private void loadData() {
		Vector<String> data = new Vector<String>();
		for (PhongBan pb : phongBanService.findAll()) {
			data.add(pb.getTenPhongBan());
		}
		comboBoxPhongBan.setModel( new DefaultComboBoxModel<String>(data));
		defaultTableModel = new DefaultTableModel();
		defaultTableModel.addColumn("Mã Phòng Ban");
		defaultTableModel.addColumn("Tên Phòng Ban");
		defaultTableModel.addColumn("Tổng Lương");
		tableLuong.setModel(defaultTableModel);

	}
