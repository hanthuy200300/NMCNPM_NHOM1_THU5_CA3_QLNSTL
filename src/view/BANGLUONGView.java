    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
    	String tenPhongBan = (String) comboBoxPhongBan.getSelectedItem();
    	PhongBan phongBan =phongBanService.findByName(tenPhongBan);
    	long tongLuong=luongService.findTongLuongByPhongBan(phongBan.getMaPhongBan());
    	Vector<String> row = new Vector<String>();
    	row.add(phongBan.getMaPhongBan());
    	row.add(phongBan.getTenPhongBan());
    	row.add(""+tongLuong);
    	defaultTableModel.addRow(row);
//    	tableLuong.setModel(defaultTableModel);
    	
    }//GEN-LAST:event_submitBtnActionPerformed

